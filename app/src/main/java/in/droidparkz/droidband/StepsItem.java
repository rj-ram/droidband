package in.droidparkz.droidband;

public class StepsItem {

    private String steps;
    private String time;

    public StepsItem(String steps, String time) {

        this.steps = steps;
        this.time = time;

    }

    public String getSteps() {
        return steps;
    }

    public String getTime() {
        return time;
    }

}